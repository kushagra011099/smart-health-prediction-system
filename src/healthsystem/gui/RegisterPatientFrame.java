    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
    if(validateInputs()==false)
    {
        JOptionPane.showMessageDialog(null," Password Cannot be left Blank","Error!",JOptionPane.ERROR_MESSAGE);  
       return ;
    }
    else
    {
        if(password.equals(Repassword))
        {
        try
        {
        user=new User(username,password,userType);
        boolean done=UserDao.registerPatient(user);
         JOptionPane.showMessageDialog(null,"Patient Registered","Sucess",JOptionPane.INFORMATION_MESSAGE);  
          AdminOptionsFrame adminframe=new AdminOptionsFrame();
          adminframe.setVisible(true);
          this.dispose();
        }
        catch(SQLException ex)
         { 
          JOptionPane.showMessageDialog(null,"Sorry! Student Cannot Be Register","ERROR!",JOptionPane.ERROR_MESSAGE);  
          ex.printStackTrace();
        }
       }
       else
       {
       JOptionPane.showMessageDialog(null,"Password does not Match!!","ERROR!",JOptionPane.ERROR_MESSAGE);
       txtPassword.setText("");
       txtRepassword.setText("");
       return;
       }
   }
    
    }//GEN-LAST:event_btnRegisterActionPerformed
    private boolean validateInputs()
  {
    username=txtUsername.getText();
    char[] pwd=txtPassword.getPassword();
    char[] rePwd=txtRepassword.getPassword();
    if(username.isEmpty() || pwd.length==0 || rePwd.length==0)
    {
        return false;
    }
    password=String.valueOf(pwd);
    Repassword=String.valueOf(rePwd);
    return true;
  }
