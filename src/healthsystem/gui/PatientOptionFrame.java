    private void btnDoTask4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoTask4ActionPerformed
    String sym1=jcSym1.getSelectedItem().toString();
    String sym2=jcSym2.getSelectedItem().toString();
    String sym3=jcSym3.getSelectedItem().toString();
     System.out.println(sym1+"\t"+ sym2+"\t"+sym3);
    try
    {
        Disease d=DiseaseDao.searchDisease(sym1, sym2, sym3);
        if(d==null)
        {
            JOptionPane.showMessageDialog(null, "Sorry!!!! You have Something Serious", "Alert",JOptionPane.ERROR_MESSAGE);
            return;
        }
        JOptionPane.showMessageDialog(null,"Hello "+UserProfile.getUsername()+" \nyou have suffring from "+d.getDname()+"\nPlease consult to our Specialist  "+d.getDoctorname(),"Alert",JOptionPane.INFORMATION_MESSAGE);
        Patient p=new Patient(UserProfile.getUsername(),d.getDname(),d.getDoctorname());
        boolean done=PatientDao.addRec(p);
        JOptionPane.showMessageDialog(null,"your Record is saved","Alert",JOptionPane.INFORMATION_MESSAGE);    
    }                                         
     catch(SQLException ex)
        {
                JOptionPane.showMessageDialog(null,"ERROR IN DB!","Patient option Error!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDoTask4ActionPerformed

