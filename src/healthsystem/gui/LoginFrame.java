    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        boolean isValidInput=validateInputs();
        if(isValidInput==false)
        {
            JOptionPane.showMessageDialog(null,"Username and Password Cannot be left Blank","Error!",JOptionPane.ERROR_MESSAGE);  
            return ;
        }
        String usertype=getUserType();
        if(usertype==null)
        {
             JOptionPane.showMessageDialog(null,"Please Select UserType!","Error!",JOptionPane.ERROR_MESSAGE);
             return;
        }
        try
        {
            User user=new User();
            user.setUserId(username);
            user.setPassword(password);
            user.setUserType(usertype);
            boolean isValidUser=UserDao.validateUser(user);
            if(isValidUser==true)
            {
                
                JOptionPane.showMessageDialog(null,"Login Accepted!","Sucess",JOptionPane.INFORMATION_MESSAGE);
                UserProfile.setUsername(username);
                UserProfile.setUsertype(usertype);
                if(usertype.equalsIgnoreCase("admin"))
                {
                    AdminOptionsFrame adminframe=new AdminOptionsFrame();
                    adminframe.setVisible(true);
                }
                else
                {
                    PatientOptionFrame pframe=new PatientOptionFrame();
                    pframe.setVisible(true);
                }
                this.dispose();
                
            }
            else
            {
                txtUserName.setText("");
                txtPassword.setText("");
                JOptionPane.showMessageDialog(null,"Invalid Username/Password!!","Login Falied!",JOptionPane.ERROR_MESSAGE);
                return;  
            }       
        }
        catch(SQLException ex)
        {
                txtUserName.setText("");
                txtPassword.setText("");
                buttonGroup2.clearSelection();
                JOptionPane.showMessageDialog(null,"ERROR IN DB!","Login Error!",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
            
          
    }//GEN-LAST:event_btnLoginActionPerformed
     
